#!/usr/bin/env python3
"""Derive a domain generation guide from prompts and pools.

Outputs:
- prompt_analysis.md
- behavior_taxonomy.md
- noise_rules.md
- dimension_design.md
- gen.md
- guide_qc.md

This is a v1 runnable implementation: each section is generated by an LLM call,
then assembled into gen.md with fixed difficulty semantics.
"""

from __future__ import annotations

import argparse
import json
import logging
import re
import sys
import time
import urllib.error
import urllib.request
from pathlib import Path
from typing import Any


LOG = logging.getLogger("derive_guide")

DEFAULT_API_BASE = "http://192.168.101.15:9898"
DEFAULT_MODEL = "qwen/qwen3.6-35b-a3b"

DIFFICULTY_SEMANTICS = """## Difficulty Semantics

- `L1`: no proactive adversarial behavior.
- `L2`: adversarial behavior is self-resolving. Even without good model
  handling, the user cools down within one or two turns; about 70 percent of L2
  adversarial actions should resolve after one turn until the next adversarial
  action.
- `L3`: adversarial behavior does not self-resolve. Unless the model responds
  effectively, the user continues adversarial behavior.
"""


def iter_jsonl(path: Path):
    with path.open(encoding="utf-8") as f:
        for line_no, line in enumerate(f, 1):
            if not line.strip():
                continue
            try:
                yield line_no, json.loads(line)
            except json.JSONDecodeError as exc:
                LOG.warning("skip invalid json line %s in %s: %s", line_no, path, exc)


def read_json_or_jsonl(path: Path) -> list[dict[str, Any]]:
    if path.suffix == ".jsonl":
        return [obj for _, obj in iter_jsonl(path)]
    data = json.loads(path.read_text(encoding="utf-8"))
    if isinstance(data, list):
        return data
    if isinstance(data, dict):
        return [data]
    raise ValueError(f"unsupported json root in {path}: {type(data).__name__}")


def compact_text(text: str, limit: int) -> str:
    if len(text) <= limit:
        return text
    half = max(1, limit // 2)
    return text[:half] + "\n...[TRUNCATED]...\n" + text[-half:]


def compact_json(data: Any, limit: int) -> str:
    return compact_text(json.dumps(data, ensure_ascii=False, indent=2), limit)


class ChatClient:
    def __init__(
        self,
        api_base: str,
        model: str,
        provider: str,
        temperature: float,
        max_tokens: int,
        retries: int,
        timeout: int,
    ) -> None:
        self.api_base = api_base.rstrip("/")
        self.model = model
        self.provider = provider
        self.temperature = temperature
        self.max_tokens = max_tokens
        self.retries = retries
        self.timeout = timeout

    def call(self, prompt: str) -> str:
        body: dict[str, Any] = {
            "model": self.model,
            "messages": [{"role": "user", "content": prompt}],
            "temperature": self.temperature,
            "max_completion_tokens": self.max_tokens,
        }
        if self.provider:
            body["provider"] = self.provider
        req = urllib.request.Request(
            f"{self.api_base}/v1/chat/completions",
            data=json.dumps(body).encode("utf-8"),
            headers={"Content-Type": "application/json"},
        )
        last_error: Exception | None = None
        for attempt in range(self.retries + 1):
            try:
                with urllib.request.urlopen(req, timeout=self.timeout) as resp:
                    data = json.loads(resp.read())
                if "choices" in data:
                    return (data["choices"][0]["message"].get("content") or "").strip()
                if "content" in data:
                    return str(data["content"]).strip()
                raise RuntimeError(f"unexpected response keys: {list(data.keys())}")
            except urllib.error.HTTPError as exc:
                body_text = exc.read().decode(errors="replace")
                last_error = RuntimeError(f"HTTP {exc.code}: {body_text[:300]}")
            except Exception as exc:  # noqa: BLE001
                last_error = exc
            if attempt < self.retries:
                time.sleep(2)
        raise RuntimeError(f"chat failed after {self.retries + 1} attempts: {last_error}")


def strip_markdown_fence(raw: str) -> str:
    raw = raw.strip()
    match = re.search(r"```(?:markdown|md)?\s*([\s\S]*?)\s*```", raw)
    if match:
        return match.group(1).strip()
    return raw


def prompt_summary(prompts: list[dict[str, Any]], limit_per_prompt: int) -> list[dict[str, Any]]:
    rows = []
    for item in prompts:
        rows.append(
            {
                "id": item.get("id") or item.get("prompt_id"),
                "category": item.get("category"),
                "scene": item.get("scene") or item.get("business"),
                "prompt_excerpt": compact_text(str(item.get("prompt") or ""), limit_per_prompt),
                "function_excerpt": compact_text(str(item.get("function") or ""), 2000),
            }
        )
    return rows


def generate_prompt_analysis(client: ChatClient, domain: str, prompts: list[dict[str, Any]], notes: str) -> str:
    prompt = f"""You are designing a benchmark generation guide for domain `{domain}`.

Analyze the assistant prompts. Focus on reusable generation rules for user profiles.

User notes:
{notes or "(none)"}

Prompt summaries:
{compact_json(prompt_summary(prompts, 5000), 60000)}

Write Markdown with these sections:
# Prompt Structure Analysis
## Domain Objective
## Common Assistant Goals
## Required Flow Patterns
## Tools and Function-Call Requirements
## Compliance / Safety Boundaries
## User Decision Points
## Identity Placeholder Rules

Be concrete and operational. Do not invent facts outside the prompts."""
    return strip_markdown_fence(client.call(prompt))


def generate_behavior_taxonomy(
    client: ChatClient,
    domain: str,
    stage2: list[dict[str, Any]],
    behavior_pool: list[dict[str, Any]],
) -> str:
    prompt = f"""You are deriving a behavior taxonomy for `{domain}` benchmark user simulation.

Use the stage2 personas and behavior_pool. Do not force generic categories if the data suggests domain-specific ones.

Stage2 personas:
{compact_json(stage2, 50000)}

Behavior pool sample:
{compact_json(behavior_pool[:200], 60000)}

Write Markdown:
# Behavior Taxonomy
## Major Behavior Types
For each type: definition, triggers, spoken signals, agent test point.
## Behavior Combinations
## Resolution / De-escalation Patterns
## Behaviors to Avoid or Not Overuse
## How to Use behavior_pool in Profile Generation

Keep it usable for generating profiles, not just descriptive."""
    return strip_markdown_fence(client.call(prompt))


def generate_noise_rules(client: ChatClient, domain: str, noise_pool: list[dict[str, Any]]) -> str:
    prompt = f"""You are deriving noise and friction rules for `{domain}` benchmark user simulation.

Noise pool sample:
{compact_json(noise_pool[:250], 60000)}

Write Markdown:
# Noise and Friction Rules
## Noise Types
## Context Requirements
## Frequency and Placement
## How Noise Should Affect the Agent
## What Not to Do
## Examples

Noise must be realistic and must not be added just to make the dialog hard."""
    return strip_markdown_fence(client.call(prompt))


def generate_dimension_design(
    client: ChatClient,
    domain: str,
    stage2: list[dict[str, Any]],
    behavior_taxonomy: str,
) -> str:
    prompt = f"""You are designing benchmark dimensions for `{domain}`.

Use stage2 personas and the behavior taxonomy to define domain-specific dimensions.

Stage2 personas:
{compact_json(stage2, 50000)}

Behavior taxonomy:
{compact_text(behavior_taxonomy, 20000)}

Write Markdown:
# Dimension Design
## user_starting_position
Define labels from the data. Do not use the old field name intent_level as the standard.
## convertibility_ceiling
Define success, failure, and intermediate outcome ceilings for this domain.
## Coverage Rules
State how profile_spec should cover user_starting_position, convertibility_ceiling, behavior types, and difficulty.
## Spec Field Recommendations
List required and optional fields.

Remember difficulty is assigned at spec/profile stage, not build-pools stage."""
    return strip_markdown_fence(client.call(prompt))


def assemble_gen_md(
    domain: str,
    prompt_analysis: str,
    behavior_taxonomy: str,
    noise_rules: str,
    dimension_design: str,
    notes: str,
) -> str:
    return f"""# Domain Guide: {domain}

This guide was derived from prompts and pools. Human review is required before
full profile generation.

## 1. Benchmark Objective

See the prompt-derived objective and domain constraints below.

{prompt_analysis}

## 2. User Starting Position and Convertibility Ceiling

{dimension_design}

## 3. Difficulty Semantics

{DIFFICULTY_SEMANTICS}

## 4. Behavior Taxonomy

{behavior_taxonomy}

## 5. Noise and Friction Rules

{noise_rules}

## 6. Profile Assembly Rules

- Generate motivation-driven profiles, not turn-by-turn scripts.
- Preserve the profile spec fields exactly: `profile_id`, `prompt_id`,
  `business`, `difficulty`, `user_starting_position` or legacy `intent_level`,
  `convertibility_ceiling`, `identity`, `resolution_style`, and
  `action_design`.
- Use stage2 personas as stable user archetypes.
- Use behavior_pool for realistic user actions and spoken phrasing.
- Use noise_pool sparingly and only with enough context.
- `task_instructions` should describe motives, triggers, reaction spectrum, and
  persistence, not exact replies.
- `behavioral_affordances` should state what the user tends to do, what they do
  not do, hard boundaries, and how they soften or escalate.
- `behavior_examples` should be short spoken fragments in the target language.

## 7. Identity Handling

- `identity: {{}}` means no prompt variables need filling or the prompt already
  mocks identity.
- `identity: {{"field": true}}` means the generator should create a realistic
  value for that placeholder.
- Concrete identity values must be preserved.
- Do not add extra identity fields that are not required by the prompt.

## 8. Output Profile Requirements

Native profiles may vary by domain, but they must be adaptable to runtime
profiles with: `profile_id`, `prompt_id`, `identity`, `persona`, `situation`,
`task_instructions`, `behavioral_affordances`, `behavior_examples`,
`ending_expected`, and `meta`.

## 9. Quality Checklist

- The profile follows the spec and does not contradict `action_design`.
- The profile is plausible for the domain.
- The profile is not scripted as "when the agent says X, say Y".
- Difficulty follows the fixed L1/L2/L3 semantics.
- `convertibility_ceiling` is respected.
- User language and behavior are grounded in the pools.
- Noise is contextual and not overused.
- Identity placeholders are handled correctly.

## 10. Common Failure Modes

- Overfitting to one prompt or business line.
- Turning behavior examples into scripts.
- Making all difficult users angry instead of varying behavior types.
- Letting L2 users sustain resistance like L3.
- Letting L3 users soften without effective assistant handling.
- Exceeding convertibility ceiling.
- Adding facts, product benefits, or policy claims not present in prompts.

## 11. User Notes

{notes or "(none)"}
"""


def generate_guide_qc(client: ChatClient, gen_md: str) -> str:
    prompt = f"""Audit this generated benchmark domain guide.

Guide:
\"\"\"
{compact_text(gen_md, 70000)}
\"\"\"

Write Markdown:
# Guide QC
## Strengths
## Blocking Issues
## Ambiguities
## Missing Inputs
## Recommended Human Review Checklist

Be strict. Identify whether this guide is safe to use for smoke generation or full generation."""
    return strip_markdown_fence(client.call(prompt))


def main() -> int:
    parser = argparse.ArgumentParser(description="Derive domain gen.md from prompts and pools")
    parser.add_argument("--domain", required=True)
    parser.add_argument("--prompts", required=True, type=Path)
    parser.add_argument("--stage2", required=True, type=Path)
    parser.add_argument("--noise-pool", required=True, type=Path)
    parser.add_argument("--behavior-pool", required=True, type=Path)
    parser.add_argument("--output", required=True, type=Path)
    parser.add_argument("--notes", default="")
    parser.add_argument("--notes-file", type=Path)
    parser.add_argument("--api-base", default=DEFAULT_API_BASE)
    parser.add_argument("--model", default=DEFAULT_MODEL)
    parser.add_argument("--provider", default="open_router")
    parser.add_argument("--temperature", type=float, default=0.3)
    parser.add_argument("--max-tokens", type=int, default=4096)
    parser.add_argument("--retries", type=int, default=2)
    parser.add_argument("--timeout", type=int, default=180)
    args = parser.parse_args()

    logging.basicConfig(
        level=logging.INFO,
        format="%(asctime)s [%(levelname)s] %(message)s",
        datefmt="%H:%M:%S",
        stream=sys.stderr,
    )

    notes = args.notes
    if args.notes_file:
        notes = args.notes_file.read_text(encoding="utf-8")

    args.output.mkdir(parents=True, exist_ok=True)
    prompts = read_json_or_jsonl(args.prompts)
    stage2 = read_json_or_jsonl(args.stage2)
    noise_pool = read_json_or_jsonl(args.noise_pool)
    behavior_pool = read_json_or_jsonl(args.behavior_pool)
    LOG.info(
        "loaded prompts=%d stage2=%d noise=%d behavior=%d",
        len(prompts),
        len(stage2),
        len(noise_pool),
        len(behavior_pool),
    )

    client = ChatClient(
        api_base=args.api_base,
        model=args.model,
        provider=args.provider,
        temperature=args.temperature,
        max_tokens=args.max_tokens,
        retries=args.retries,
        timeout=args.timeout,
    )

    LOG.info("generating prompt_analysis.md")
    prompt_analysis = generate_prompt_analysis(client, args.domain, prompts, notes)
    (args.output / "prompt_analysis.md").write_text(prompt_analysis + "\n", encoding="utf-8")

    LOG.info("generating behavior_taxonomy.md")
    behavior_taxonomy = generate_behavior_taxonomy(client, args.domain, stage2, behavior_pool)
    (args.output / "behavior_taxonomy.md").write_text(behavior_taxonomy + "\n", encoding="utf-8")

    LOG.info("generating noise_rules.md")
    noise_rules = generate_noise_rules(client, args.domain, noise_pool)
    (args.output / "noise_rules.md").write_text(noise_rules + "\n", encoding="utf-8")

    LOG.info("generating dimension_design.md")
    dimension_design = generate_dimension_design(client, args.domain, stage2, behavior_taxonomy)
    (args.output / "dimension_design.md").write_text(dimension_design + "\n", encoding="utf-8")

    gen_md = assemble_gen_md(
        args.domain,
        prompt_analysis,
        behavior_taxonomy,
        noise_rules,
        dimension_design,
        notes,
    )
    (args.output / "gen.md").write_text(gen_md + "\n", encoding="utf-8")

    LOG.info("generating guide_qc.md")
    guide_qc = generate_guide_qc(client, gen_md)
    (args.output / "guide_qc.md").write_text(guide_qc + "\n", encoding="utf-8")

    manifest = {
        "domain": args.domain,
        "prompts": str(args.prompts),
        "stage2": str(args.stage2),
        "noise_pool": str(args.noise_pool),
        "behavior_pool": str(args.behavior_pool),
        "model": args.model,
        "provider": args.provider,
        "outputs": [
            "prompt_analysis.md",
            "behavior_taxonomy.md",
            "noise_rules.md",
            "dimension_design.md",
            "gen.md",
            "guide_qc.md",
        ],
    }
    (args.output / "derive_guide_manifest.json").write_text(
        json.dumps(manifest, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    LOG.info("wrote guide outputs to %s", args.output)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
